public class EjercicioArray2 {
    public static void main(String[] args) throws Exception {
       String [] simbolo = new String[9];
       simbolo [0] = "a";
       simbolo [1] = "x";
       simbolo [4] = "@";
       simbolo [6] = " ";
       simbolo [7] = "+";
       simbolo [8] = "Q";

       for (int i = 0; i < 9; i++) {
        System.out.println(simbolo[i]);
        }
    }
}
